number1 = int(input("Enter base number:"))
number2 = int(input("Enter exponent: "))
result = number1 ** number2
print(f"{number1} to the power of {number2} is {result}")
number3 = int(input("Enter another base: "))
number4 = int(input("Enter another exponent:"))
result2 = pow(number3, number4)
print(f"{number3} to the power of {number4} is {result2}")